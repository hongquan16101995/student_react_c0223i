export class Student {
    id
    name
    age
    gender
    address
    avatar
    math
    physic
    chemistry

    constructor(id, name, age, gender, address, avatar, math, physic, chemistry) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.address = address;
        this.avatar = avatar;
        this.math = math;
        this.physic = physic;
        this.chemistry = chemistry;
    }
}
